package conversion;

public class TypeConversion {

	public void test() {
		
		System.out.println("Empty");
		
	}
	
	public void test(int num1, int num2) {
		
		System.out.println("int num 1: " + num1 + " int num 2: " + num2);
		
	}
	
	public void test(double num) {
		
		System.out.println("Casting num to double: " + num);
		
	}

}
