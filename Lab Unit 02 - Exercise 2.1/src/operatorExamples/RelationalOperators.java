package operatorExamples;

public class RelationalOperators {
	
	int x = 8;
	int y = 9;
	
	public void printExample4() {
		
		System.out.println("Relational Operators: \n");
		
		System.out.println("9 > 3 = "+ (x > y));
		System.out.println("9 < 3 = "+ (x < y));
		System.out.println("9 >= 3 = "+ (x >= y));
		System.out.println("9 <= 3 = "+ (x <= y));
		System.out.println("9 == 3 = "+ (x == y));
		System.out.println("9 != 3 = "+ (x != y));
		
		System.out.print("\n");
		
	}

}
