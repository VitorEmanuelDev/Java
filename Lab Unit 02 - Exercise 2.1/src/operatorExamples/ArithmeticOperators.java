package operatorExamples;

public class ArithmeticOperators {
	
	int x = 9;
	int y = 3;
	
	public void printExample3() {
		
		System.out.println("Arithmetic Operators: \n");
		
		System.out.println("9 + 3 = "+ (x + y));
		System.out.println("9 - 3 = "+ (x - y));
		System.out.println("9 * 3 = "+ (x * y));
		System.out.println("9 / 3 = "+ (x / y));
		System.out.println("9 % 3 = "+ (x % y));
		
		System.out.print("\n");
		
	}

}
