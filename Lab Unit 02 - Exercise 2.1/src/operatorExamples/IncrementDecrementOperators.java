package operatorExamples;

public class IncrementDecrementOperators {
	
	int test1 = 0;
	
	public void printExample1() {
		
		System.out.println("Increment and decrement examples:\n");
		
		System.out.println("Before increment = " + test1);
		test1++;
		System.out.println("After increment = " + test1);
		System.out.println("Before decrement = " + test1);
		test1--;
		System.out.println("After decrement = " + test1);
		
		System.out.print("\n");

		
	}

}
